def gen_frame_num(input_list):
    # 生成帧序列
    frame_sequences = []
    for start, end in input_list:
        # 使用range生成序列，从start开始，到end+1(包含end)，步长为5
        sequence = list(range(start, end + 1))  # 步长为1
        # sequence = list(range(start, end + 1, 5))  # 步长为5
        # 如果最后一个元素不是end，则添加end
        if sequence[-1] != end:
            sequence.append(end)
        frame_sequences.append(sequence)
    print(len(frame_sequences), frame_sequences)
    return frame_sequences


def get_sub_video_name(pre_name, input_list):
    sub_name = []
    for i, sub_video in enumerate(input_list):
        # print(f"第 {i} 个子视频的帧范围为：{sub_video}")
        sub_name.append(f"{pre_name}_{sub_video[0]}_{sub_video[1]}")
    print(len(sub_name), sub_name)
    return sub_name


def remove_frame(data):
    dict_wo_frame = {}
    for key, value in data.items():
        # Process each coordinate list in the value array
        processed_coords = []
        for coord in value:
            # Remove the first element (frame number) and keep the rest
            processed_coord = coord[1:]
            processed_coords.append(processed_coord)
        dict_wo_frame[key] = processed_coords
    return dict_wo_frame


