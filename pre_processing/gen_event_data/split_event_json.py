import os
import json
import argparse


def read_segments_from_file(file_path):
    """
    从文件中读取视频片段信息

    Args:
        file_path: 包含片段信息的文件路径

    Returns:
        每个长视频的片段列表
    """
    with open(file_path, 'r') as f:
        content = f.read()

    # 解析segments_list
    segments_list = eval(content)
    return segments_list


def split_json_files(input_folder, output_folder, segments_list):
    """
    将长视频事件JSON文件切割成短视频事件JSON文件

    Args:
        input_folder: 包含长视频事件JSON文件的文件夹
        output_folder: 保存分割后JSON文件的输出文件夹
        segments_list: 每个长视频的片段列表

    Returns:
        None (将输出JSON文件写入磁盘)
    """
    # 确保输出文件夹存在
    os.makedirs(output_folder, exist_ok=True)

    # 遍历每个长视频及其对应的JSON文件
    for i, segments in enumerate(segments_list):
        # 长视频编号从06开始
        long_video_number = i + 6
        json_filename = f"IMG_01{long_video_number:02d}_game_events.json"
        json_filepath = os.path.join(input_folder, json_filename)

        # 检查文件是否存在
        if not os.path.exists(json_filepath):
            print(f"文件 {json_filepath} 不存在，跳过")
            continue

        # 读取长视频的JSON文件
        with open(json_filepath, 'r') as f:
            events = json.load(f)

        # 初始化每个短视频段落的事件列表
        segment_events_list = [[] for _ in range(len(segments))]

        # 遍历长视频中的每个事件
        for event in events:
            # 确保帧号是整数类型
            event_start = int(event['start_frame'])
            event_end = int(event['end_frame'])

            # 找到包含事件开始帧的短视频段落
            segment_index = -1
            for j, segment in enumerate(segments):
                # 确保段落边界是整数类型
                segment_start = int(segment[0])
                segment_end = int(segment[1])

                if segment_start <= event_start <= segment_end:
                    segment_index = j
                    break

            # 如果找不到包含事件开始帧的短视频段落，则跳过这个事件
            if segment_index == -1:
                continue

            # 获取该事件所属的短视频段落
            # 再次确保段落边界是整数类型
            segment_start = int(segments[segment_index][0])
            segment_end = int(segments[segment_index][1])

            # 创建事件的副本
            new_event = event.copy()

            # 调整帧号并确保是整数类型
            new_event['start_frame'] = int(event_start - segment_start + 1)
            new_event['end_frame'] = int(min(event_end, segment_end) - segment_start + 1)

            # 将事件添加到对应的短视频段落的事件列表中
            segment_events_list[segment_index].append(new_event)

        # 为每个短视频段落创建一个新的JSON文件
        for j, segment_events in enumerate(segment_events_list):
            # 获取段落的开始和结束帧号
            segment_start = int(segments[j][0])
            segment_end = int(segments[j][1])

            # 创建输出文件名 (例如: IMG_0106_150_647_events.skeleton_mmpose_format)
            output_filename = f"IMG_01{long_video_number:02d}_{segment_start}_{segment_end}_events.json"
            output_filepath = os.path.join(output_folder, output_filename)

            # 写入新的JSON文件
            with open(output_filepath, 'w') as f:
                json.dump(segment_events, f, indent=4)

            print(f"已创建文件 {output_filepath}，包含 {len(segment_events)} 个事件")


